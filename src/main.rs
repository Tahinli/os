fn example_test() {
    assert_eq!(1, 1);
}

#[panic_handler]
pub fn panic_handler(info: &PanicInfo) -> ! {
    println!("{}", info);
    loop {}
}

#[unsafe(no_mangle)]
pub extern "C" fn _start() -> ! {
    println!("Hello from TahinliOS!");

    #[cfg(test)]
    test_main();

    loop {
        panic!("Panic or not, it's the whole thing.");
    }
}
